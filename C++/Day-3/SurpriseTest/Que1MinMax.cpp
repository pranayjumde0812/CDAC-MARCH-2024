//Q1. Write a C Program to find the Maximum and minimum of two numbers without 
//using any loop or condition.

#include<iostream>
#include<bits/stdc++.h>

using namespace std;

int main(){
	
	int a = 25,b = 30;
	
	cout<<"The minimum number from a and b = "<<min(a,b)<<endl;
	
	cout<<"The maximum number from a and b = "<<max(a,b)<<endl;
	
	
	return 0;
}
